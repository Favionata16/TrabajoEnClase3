
package LabTc3;

public class Transporte {
    protected int placa;
    protected String color;

    public Transporte(int placa, String color) {
        this.placa = placa;
        this.color = color;
    }

    public int getPlaca() {
        return placa;
    }

    public void setPlaca(int placa) {
        this.placa = placa;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    
    
    
    
}
